def taxi_bill(km):
    cost=0
    if km<=0.3:
        cost=20000*km
    elif km<=3:
        cost=20000*0.3 + (km-0.3)*18600
    elif km<=11:
        cost=20000*0.3 + (3-0.3)*18600 + (km-3)*14200
    elif km<=20:
        cost=20000*0.3 + (3-0.3)*18600 + (11-3)*14200 + (km-11)*13000
    elif km<=30:
        cost=20000*0.3 + (3-0.3)*18600 + (11-3)*14200 + (20-11)*13000 + (km-20)*12000
    else:
        cost=20000*0.3 + (3-0.3)*18600 + (11-3)*14200 + (20-11)*13000 + (30-20)*12000 + (km-30)*11000
    if km>120:
        cost*=90/100
    return cost*105/100

n=int(input())
print('{}'.format('%d'%taxi_bill(n)))